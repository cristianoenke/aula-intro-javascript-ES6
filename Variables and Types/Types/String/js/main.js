const textOriginal = 'Texto'
console.log(`Original variable: "${textOriginal}"`);

// Tamanho de uma string
const textSize = 'Texto'.length;
console.log(`Length of variable: "${textSize}"`);
    
// Array Splitting
const textSplit = 'Texto'.split('x');
console.log(`Splitted variable: "${textSplit}"`);
    
// Busca e Substituição
const textReplace = 'Texto'.replace('Texto','T e x t o');
console.log(`Substitution of variable: "${textReplace}"`);

// Array Slicing
const textSlice = 'Texto'.slice(2,4);
console.log(`Variable from index 2 to 4: "${textSlice}"`);
    
// Substring
const textSubstring = 'Texto'.substr(0,2);
console.log(`Substring of variable: "${textSubstring}"`);
    